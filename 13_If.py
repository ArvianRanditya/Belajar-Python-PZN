# IF MERUPAKAN ATRIBUT KONDISI, JIKA SESUATU YANG DI MINTA IF TERPENUHI MAKA AKAN TRUE DAN JIKA TIDAK MAKA AKAN FALSE
orang = 100
zombie = 100

if orang < zombie:
  print("Kaburr, dunia akan berakhir")

if orang > zombie:
  print("Ayoo, lawan zombie")

if orang == zombie:
  print("Bersiap kita akan berperang")