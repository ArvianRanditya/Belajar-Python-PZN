# OPERATOR PERBANDINGAN

# > LEBIH DARI
# < KURANG DARI
# >= LEBIH DARI SAMA DENGAN
# <= KURANG DARI SAMA DENGAN
# == SAMA DENGAN
# != TIDAK SAMA DENGAN
# HASILL DARI OPERATOR PERBANDINGAN BERNILAI BOOLEAN TRUE/FALSE

print(2 > 1)
print(2 > 4)
print(" ")
print(1 < 3)
print(3 < 1)
print(" ")
print(1 >= 1)
print(1 <= 1)
print(" ")
print(1 == 1)
print(1 == 2)
print(" ")
print(1 != 2)
print(1 != 1)
print(" ")

# OPERATOR == DAN != BISA DIGUNAKAN UNTUK STRING
print("EKO" == "EKO")
print("EKO" != "EKO")